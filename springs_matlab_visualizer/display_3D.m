	'XLim' , [ -1 34 ] , ...
	'YLim' , [ -1 34 ] , ...
	'ZLim' , [ -1 34 ] , ...
	'View' , [0,90],...%[ -25 , 25 ] , ...%
	'DataAspectRatio' , [1,1,1] , ...
	'NextPlot' , 'add' )
