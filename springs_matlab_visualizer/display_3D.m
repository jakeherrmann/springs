zz = [ nodes.position( springs.nodes(:,1) ,3) , nodes.position( springs.nodes(:,2) ,3) ] ;
xx(:,3) = nan ;
yy(:,3) = nan ;
zz(:,3) = nan ;
plot3( ...
	reshape( xx' ,[],1) , ...
	reshape( yy' ,[],1) , ...
	reshape( zz' ,[],1) , ...
	'k-' , ...
	'LineWidth' , 1 )
